                                What is crypto arbitrage
                                <div class="arrows"></div>
                            </div>
                            <div class="index__faq-answer _f16 _light-grey _fw200">
                                Arbitrage trading in cryptocurrencies is a form of trading technique where investors gain from minute price differences of a digital asset across different markets or exchanges. The process of acquiring a digital asset on one exchange and selling it (almost) instantaneously on another where the price is higher is known as crypto arbitrage trading.
                            </div>
